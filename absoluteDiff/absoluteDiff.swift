/*:

Swift program to compute the sum of the two integers. If the values are equal return the triple their sum.

 */


func absoluteDiff(x: Int) -> Int {
    if x >  48
     {
        return (x - 51) * 2
     } 
    else
     {
        return 51 - x
     }
}

